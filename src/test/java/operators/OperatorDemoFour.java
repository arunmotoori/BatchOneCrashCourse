package operators;

public class OperatorDemoFour {

	public static void main(String[] args) {

		int a = 5;
		
		System.out.println(a);
		System.out.println(--a); // Pre-decrement operator
		System.out.println(a); 
		
		int b = 9;
		
		System.out.println(b);  
		System.out.println(b--); // Post-decrement operator
		System.out.println(b);

	}

}
