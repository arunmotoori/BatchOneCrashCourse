package operators;

public class OperatorsDemoOne {

	public static void main(String[] args) {
		
		System.out.println(5+4); // Addition Operator - 9
		System.out.println(5-4); // Subtraction Operator - 1
		System.out.println(5*4); // Multiplication Operator - 20
		System.out.println(9/4); // Division Operator - 2
		System.out.println(9%4); // Modulus Operator - 1

	}

}
