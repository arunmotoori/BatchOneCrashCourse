package operators;

public class OperatorsDemoEight {

	public static void main(String[] args) {
		
		/*
		int a = 5;
		int b = 4;
		int c;
		*/
		
		int a = 5,b = 4,c;
		
		a+=b; // Compound Assignment operator - a = a + b;
		
		System.out.println(a);  // 9
		
		a-=b; // a = a - b
		
		System.out.println(a);  // 5
		
		a*=b; // a = a * b;
		
		System.out.println(a); // 20
		
		a/=b; // a = a / b; 
		
		System.out.println(a);  // 5
		
		a%=b; // a = a % b;
		
		System.out.println(a);  // 1
		
		float x = 3.45F;
		float y = 3.4F;
		double z = 3.4F;

	}

}
