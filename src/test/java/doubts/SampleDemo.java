package doubts;

public class SampleDemo {

	public static void main(String[] args) {
		
		int a = (int)3.5;  // Explicit Type Casting
		System.out.println(a);
		
		double b = 9; // Implicit Type Casting
		System.out.println(b); 
		
		int c = 's'; // Implicit Type Casting
		System.out.println(c);
		
		char d = 115; // Implicit Type Casting
		System.out.println(d);
		
		//boolean b = (boolean)"Arun";
		
	}

}
