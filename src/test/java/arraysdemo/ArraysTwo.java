package arraysdemo;

public class ArraysTwo {

	public static void main(String[] args) {
		
		//int[] ar = new int[3]; // Actual Representation
		
		int[] ar = {9,5,7};  // Shortcut Representation
		
		System.out.println(ar[0]);  // 9
		System.out.println(ar[1]);  // 5
		System.out.println(ar[2]);  // 7

	}

}
