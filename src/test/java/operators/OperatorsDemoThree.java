package operators;

public class OperatorsDemoThree {

	public static void main(String[] args) {
		
		int a = 5;
				
		System.out.println(a);
		System.out.println(++a); // Pre-increment operator
		System.out.println(a); // 

		int b = 9;
		
		System.out.println(b);
		System.out.println(b++); // Post-increment operator
		System.out.println(b); 
		
		
	}

}
